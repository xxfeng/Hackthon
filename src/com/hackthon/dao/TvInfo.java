package com.hackthon.dao;

public class TvInfo {
	public String NAME = "name";
	public String TYPE = "type";
	public String ABSTRACTS = "abstracts";
	public String SERIES_NUM = "seriesNum";
	public String IMAGE_URL = "imageUrl";
	public String PDATES = "pdates";
	public String ACTORS = "actors";
	public String REGION = "region";
	
	
	public String getNAME() {
		return NAME;
	}
	public void setNAME(String nAME) {
		NAME = nAME;
	}
	public String getTYPE() {
		return TYPE;
	}
	public void setTYPE(String tYPE) {
		TYPE = tYPE;
	}
	public String getABSTRACTS() {
		return ABSTRACTS;
	}
	public void setABSTRACTS(String aBSTRACTS) {
		ABSTRACTS = aBSTRACTS;
	}
	public String getSERIES_NUM() {
		return SERIES_NUM;
	}
	public void setSERIES_NUM(String sERIES_NUM) {
		SERIES_NUM = sERIES_NUM;
	}
	public String getIMAGE_URL() {
		return IMAGE_URL;
	}
	public void setIMAGE_URL(String iMAGE_URL) {
		IMAGE_URL = iMAGE_URL;
	}
	public String getPDATES() {
		return PDATES;
	}
	public void setPDATES(String pDATES) {
		PDATES = pDATES;
	}
	public String getACTORS() {
		return ACTORS;
	}
	public void setACTORS(String aCTORS) {
		ACTORS = aCTORS;
	}
	public String getREGION() {
		return REGION;
	}
	public void setREGION(String rEGION) {
		REGION = rEGION;
	}
	
}
